export function getCategoryBySlug(slug: string): CategoryInfo | undefined {
  return categories.find((cat) => cat.slug.toLowerCase() === slug.toLowerCase());
}

export function getCategoryName(slug: string): string {
  const category = getCategoryBySlug(slug);
  return category?.name || slug.charAt(0).toUpperCase() + slug.slice(1);
}

export function getCategoryDescription(slug: string): string {
  const category = getCategoryBySlug(slug);
  return category?.description || `Explore ${getCategoryName(slug)} projects`;
}

